package BitManipulaltion;

// import java.util.*;
public class BitWiseOperator {
     public static void main(String args[]){
          System.out.println("Binary AND="+ (5&6));
          System.out.println("Binary OR="+ (5|6));
          System.out.println("Binary XOR="+ (5^6));
          System.out.println("Binary One's complement="+ (~5));
          System.out.println("Binary One's complement="+ (~7));
          System.out.println("Binary Left shift="+ (8<<3));
          System.out.println("Binary Right shift="+ (9>>3));
     }
}
